function Get-IdentityNowSourceAccounts {
    <#
.SYNOPSIS
    Get IdentityNow Accounts on a Source.

.DESCRIPTION
    Gets IdentityNow Accounts on a Source

.PARAMETER sourceID
    (required) The ID of an IdentityNow Source. eg. 45678

.PARAMETER attributes
    (optional -Switch) defaults to False. If specified each account on the Source is queried to obtain their attributes
    NOTE: For large sources this will take time. 

.EXAMPLE
    Get-IdentityNowSourceAccounts -sourceID 12345

.LINK
    http://darrenjrobinson.com/sailpoint-identitynow

#>

    [cmdletbinding()]
    param(
        [Parameter(Mandatory = $true, ValueFromPipeline = $true)]
        [string]$sourceID,
        [switch]$attributes,
        [int]$searchLimit = "250"
    )

    $v3Token = Get-IdentityNowAuth

    if ($v3Token.access_token) {
        $sourceObjects = @()
        $offset = 0
        $i = 0
        do { 
            # Get Next Page
            [int]$offset = $i * $searchLimit           
            do {
                $results = $null
                try {
                    $results = Invoke-RestMethod -Method Get -Uri "https://$($IdentityNowConfiguration.orgName).api.identitynow.com/cc/api/source/getAccounts?id=$($sourceID)&limit=$($searchLimit)&start=$($offset)" -Headers @{Authorization = "$($v3Token.token_type) $($v3Token.access_token)" }
                }
                catch {
                    write-host "Sleeping 2 seconds:$($_)"
                    Start-Sleep -Seconds 2
                }
            } until ($null -ne $results.items)
            if ($results) {
                $sourceObjects += $results.items
            }
            Write-Verbose "iteration = $i ; searchlimit = $searchLimit ; offset = $offset ; results = $($sourceObjects.count)"
            $i++ 
        } until ($results.items.Count -lt $searchLimit)

        if ($attributes) {
            $temp = $sourceObjects
            $sourceObjects = @()
            $i = 0
            $currenterroraction = $ErrorActionPreference
            $ErrorActionPreference = 'continue'
            
            foreach ($object in $temp) {
                $i++
                Write-Progress -Activity "fetching account attributes $($i) of $($temp.count)" -PercentComplete ($i / $temp.count * 100)
                do {
                    $result = $null
                    try {
                        $result = Invoke-RestMethod -Method Get -Uri "https://$($IdentityNowConfiguration.orgName).api.identitynow.com/beta/accounts/$($object.id)" -Headers @{Authorization = "$($v3Token.token_type) $($v3Token.access_token)" }

                    }
                    catch {
                        write-host "Sleeping 2 seconds:$($_)"
                        Start-Sleep -Seconds 2

                        # Trigger Token expiry check and refresh
                        $v3Token = $null 
                        $v3Token = Get-IdentityNowAuth
                    }
                }until($null -ne $result)
                
                $sourceObjects += $result

                # Trigger Token expiry check and refresh
                $v3Token = Get-IdentityNowAuth
            }
            $ErrorActionPreference = $currenterroraction
        }
        return $sourceObjects
    }
}


